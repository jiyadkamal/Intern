"use client";

import React from "react";

interface ChipProps {
    children: React.ReactNode;
    icon?: React.ReactNode;
    active?: boolean;
    onClick?: () => void;
}

export default function Chip({ children, icon, active = false, onClick }: ChipProps) {
    return (
        <button
            className={`chip ${active ? "active" : ""}`}
            onClick={onClick}
            type="button"
        >
            {icon && <span className="chip__icon">{icon}</span>}
            {children}
        </button>
    );
}

interface FilterChipsProps {
    filters: Array<{
        label: string;
        value: string;
        icon?: React.ReactNode;
    }>;
    activeFilter: string;
    onFilterChange: (value: string) => void;
}

export function FilterChips({ filters, activeFilter, onFilterChange }: FilterChipsProps) {
    return (
        <div className="filter-chips">
            {filters.map((filter) => (
                <Chip
                    key={filter.value}
                    icon={filter.icon}
                    active={activeFilter === filter.value}
                    onClick={() => onFilterChange(filter.value)}
                >
                    {filter.label}
                </Chip>
            ))}
        </div>
    );
}

